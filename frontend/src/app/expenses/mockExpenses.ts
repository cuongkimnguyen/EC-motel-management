// ─── Helper functions ─────────────────────────────────────────────────────────
export function formatCurrencyVND(amount: number): string {
  return new Intl.NumberFormat('vi-VN', { style: 'currency', currency: 'VND' }).format(amount);
}

export function formatDateVN(dateStr: string): string {
  const d = new Date(dateStr);
  return d.toLocaleDateString('vi-VN', { day: '2-digit', month: '2-digit', year: 'numeric' });
}

export function getPaymentStatusColor(status: ExpensePaymentStatus): string {
  switch (status) {
    case 'Đã thanh toán': return 'bg-emerald-100 text-emerald-700 border border-emerald-200';
    case 'Chưa thanh toán': return 'bg-red-100 text-red-700 border border-red-200';
    case 'Chờ xử lý': return 'bg-amber-100 text-amber-700 border border-amber-200';
    default: return 'bg-slate-100 text-slate-600';
  }
}

export function getCategoryColor(category: ExpenseCategory): string {
  switch (category) {
    case 'Điện nước': return 'bg-blue-100 text-blue-700';
    case 'Internet': return 'bg-purple-100 text-purple-700';
    case 'Vệ sinh': return 'bg-teal-100 text-teal-700';
    case 'Sửa chữa': return 'bg-orange-100 text-orange-700';
    case 'Mua sắm': return 'bg-indigo-100 text-indigo-700';
    case 'Lương / quản lý': return 'bg-pink-100 text-pink-700';
    case 'Chi phí khác': return 'bg-slate-100 text-slate-600';
    default: return 'bg-slate-100 text-slate-600';
  }
}
